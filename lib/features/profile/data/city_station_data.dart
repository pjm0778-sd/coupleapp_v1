/// 도시에서 최적 역/터미널 자동 선택
/// 우선순위: KTX > SRT > 일반역 > 버스터미널
String? getBestStation(String city) {
  final stations = cityStations[city];
  if (stations == null || stations.isEmpty) return null;
  // KTX 우선
  final ktx = stations.firstWhere(
    (s) => s.contains('KTX'),
    orElse: () => '',
  );
  if (ktx.isNotEmpty) return ktx;
  // SRT 차선
  final srt = stations.firstWhere(
    (s) => s.contains('SRT'),
    orElse: () => '',
  );
  if (srt.isNotEmpty) return srt;
  // 그 외 첫 번째
  return stations.first;
}

/// 도시가 속한 도/광역시 반환
String? getProvinceOfCity(String city) {
  for (final entry in provinceRegions.entries) {
    if (entry.value.contains(city)) return entry.key;
  }
  return null;
}
